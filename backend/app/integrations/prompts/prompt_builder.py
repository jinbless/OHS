# ── 데이터 로드 ────────────────────────────────────────────

@lru_cache(maxsize=1)
def _load_taxonomy() -> dict:
    with open(_DATA_DIR / "hazard_taxonomy.json", "r", encoding="utf-8") as f:
        return json.load(f)


@lru_cache(maxsize=1)
def _load_chapters() -> list:
    with open(_DATA_DIR / "article_chapters.json", "r", encoding="utf-8") as f:
        return json.load(f)["chapters"]


