def build_article_structure_section() -> str:
    """article_chapters.json에서 장 구조 + ARTICLE_DETAILS 결합."""
    chapters = _load_chapters()
    lines = [
        "## 산업안전보건기준에 관한 규칙 — 전체 조문 구조",
        "",
        "위험요소에 관련된 법조항 번호를 related_article_hints에 추천하세요.",
        "아래 전체 조문 구조를 참고하여 **가장 구체적으로** 관련되는 조문을 선택하세요.",
        "포괄적 조문(제42조 등)보다 **구체적 상황에 맞는 세부 조문**을 우선 추천하세요.",
        "",
    ]

    current_part = None
    for ch in chapters:
        part = ch["part"]
        chapter_name = ch["chapter"]
        r = ch["article_range"]

        if part != current_part:
            lines.append(f"### {part}")
            lines.append("")
            current_part = part

        lines.append(f"#### {chapter_name} (제{r[0]}조~제{r[1]}조)")

        # 큐레이션된 조문 상세 추가
        details = ARTICLE_DETAILS.get(chapter_name, [])
        for detail in details:
            lines.append(f"- {detail}")

        lines.append("")

    return "\n".join(lines)


